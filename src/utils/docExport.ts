const docDate = (ar: boolean) =>
  new Date().toLocaleDateString(ar ? 'ar-SA' : 'en-GB', { year: 'numeric', month: 'long', day: 'numeric' })

const refNumber = (template: string) => `${template}-2026-${String(Math.floor(1000 + Math.random() * 9000))}`

