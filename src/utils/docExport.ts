const CRC_TABLE = (() => {
  const t = new Uint32Array(256)
  for (let n = 0; n < 256; n++) {
    let c = n
    for (let k = 0; k < 8; k++) c = c & 1 ? 0xedb88320 ^ (c >>> 1) : c >>> 1
    t[n] = c >>> 0
  }
  return t
})()

function crc32(data: Uint8Array): number {
  let c = 0xffffffff
  for (let i = 0; i < data.length; i++) c = CRC_TABLE[(c ^ data[i]) & 0xff] ^ (c >>> 8)
  return (c ^ 0xffffffff) >>> 0
}

/** Builds an uncompressed ("stored") ZIP — valid for OOXML packages. */
function zipStore(files: { name: string; data: Uint8Array }[]): Blob {
  const enc = new TextEncoder()
  const parts: Uint8Array[] = []
  const central: Uint8Array[] = []
  let offset = 0

  for (const f of files) {
    const name = enc.encode(f.name)
    const crc = crc32(f.data)

    const local = new Uint8Array(30 + name.length)
    const lv = new DataView(local.buffer)
    lv.setUint32(0, 0x04034b50, true)
    lv.setUint16(4, 20, true)
    lv.setUint16(6, 0x0800, true) // UTF-8 names
    lv.setUint16(8, 0, true) // stored (no compression)
    lv.setUint32(14, crc, true)
    lv.setUint32(18, f.data.length, true)
    lv.setUint32(22, f.data.length, true)
    lv.setUint16(26, name.length, true)
    local.set(name, 30)
    parts.push(local, f.data)

    const cen = new Uint8Array(46 + name.length)
    const cv = new DataView(cen.buffer)
    cv.setUint32(0, 0x02014b50, true)
    cv.setUint16(4, 20, true)
    cv.setUint16(6, 20, true)
    cv.setUint16(8, 0x0800, true)
    cv.setUint16(10, 0, true)
    cv.setUint32(16, crc, true)
    cv.setUint32(20, f.data.length, true)
    cv.setUint32(24, f.data.length, true)
    cv.setUint16(28, name.length, true)
    cv.setUint32(42, offset, true)
    cen.set(name, 46)
    central.push(cen)

    offset += local.length + f.data.length
  }

  const cdSize = central.reduce((s, c) => s + c.length, 0)
  const eocd = new Uint8Array(22)
  const ev = new DataView(eocd.buffer)
  ev.setUint32(0, 0x06054b50, true)
  ev.setUint16(8, files.length, true)
  ev.setUint16(10, files.length, true)
  ev.setUint32(12, cdSize, true)
  ev.setUint32(16, offset, true)

  const all = [...parts, ...central, eocd]
  const total = all.reduce((s, a) => s + a.length, 0)
  const merged = new Uint8Array(total)
  let pos = 0
  for (const chunk of all) {
    merged.set(chunk, pos)
    pos += chunk.length
  }
  return new Blob([merged], {
    type: 'application/vnd.openxmlformats-officedocument.wordprocessingml.document',
  })
}

type ParOpts = {
  bold?: boolean
  size?: number // half-points
  color?: string
  align?: 'center' | 'both'
  after?: number // twips
  borderBottom?: boolean
  rtl?: boolean
}

function par(text: string, o: ParOpts = {}): string {
  const pPr =
    '<w:pPr>' +
    (o.borderBottom ? '<w:pBdr><w:bottom w:val="single" w:sz="18" w:space="6" w:color="047857"/></w:pBdr>' : '') +
    (o.rtl ? '<w:bidi/>' : '') +
    `<w:spacing w:after="${o.after ?? 120}" w:line="300" w:lineRule="auto"/>` +
    (o.align ? `<w:jc w:val="${o.align}"/>` : '') +
    '</w:pPr>'
  const sz = o.size ?? 22
  const rPr =
    '<w:rPr>' +
    '<w:rFonts w:ascii="Calibri" w:hAnsi="Calibri" w:cs="Arial"/>' +
    (o.bold ? '<w:b/><w:bCs/>' : '') +
    (o.color ? `<w:color w:val="${o.color}"/>` : '') +
    `<w:sz w:val="${sz}"/><w:szCs w:val="${sz}"/>` +
    (o.rtl ? '<w:rtl/>' : '') +
    '</w:rPr>'
  return `<w:p>${pPr}<w:r>${rPr}<w:t xml:space="preserve">${esc(text)}</w:t></w:r></w:p>`
}

/** Exported for validation tests as well as the download handler. */
export function buildDocxBlob(title: string, template: string, type: string): Blob {
  const ar = isArabic(title)
  const L = strings(ar)
  const date = docDate(ar)
  const ref = refNumber(template)

  const body: string[] = [
    par(L.org, { bold: true, size: 28, color: '065F46', rtl: ar, after: 40 }),
    par(L.orgSub, { size: 18, color: '64748B', rtl: ar, after: 80 }),
    par(`${L.kingdom} · ${L.refNo}: ${ref} · ${L.dateL}: ${date}`, { size: 18, color: '475569', rtl: ar, after: 240, borderBottom: true }),
    par(title, { bold: true, size: 32, color: '0F172A', rtl: ar, after: 60 }),
    par(`${type} · ${template}`, { bold: true, size: 18, color: '047857', rtl: ar, after: 80 }),
    par(`${L.classification}: ${L.internal} · ${L.status}: ${L.draft}`, { size: 18, color: '64748B', rtl: ar, after: 280 }),
    par(L.s1, { bold: true, size: 24, color: '065F46', rtl: ar, after: 80 }),
    par(L.p1, { size: 22, color: '334155', align: 'both', rtl: ar, after: 200 }),
    par(L.s2, { bold: true, size: 24, color: '065F46', rtl: ar, after: 80 }),
    par(`•  ${L.b1}`, { size: 22, color: '334155', rtl: ar, after: 80 }),
    par(`•  ${L.b2}`, { size: 22, color: '334155', rtl: ar, after: 80 }),
    par(`•  ${L.b3}`, { size: 22, color: '334155', rtl: ar, after: 200 }),
    par(L.s3, { bold: true, size: 24, color: '065F46', rtl: ar, after: 80 }),
    par(L.p3, { size: 22, color: '334155', align: 'both', rtl: ar, after: 280 }),
    par(L.disclaimer, { size: 18, color: '166534', rtl: ar, after: 600 }),
    par('________________________', { size: 22, color: '334155', align: 'center', after: 40 }),
    par(`${L.sig1} — ${L.sigName}`, { size: 18, color: '475569', align: 'center', rtl: ar, after: 400 }),
    par('________________________', { size: 22, color: '334155', align: 'center', after: 40 }),
    par(`${L.sig2} — ${L.sigName}`, { size: 18, color: '475569', align: 'center', rtl: ar, after: 40 }),
  ]

  const documentXml =
    '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>' +
    '<w:document xmlns:w="http://schemas.openxmlformats.org/wordprocessingml/2006/main">' +
    `<w:body>${body.join('')}` +
    '<w:sectPr><w:pgSz w:w="11906" w:h="16838"/><w:pgMar w:top="1134" w:right="1134" w:bottom="1134" w:left="1134" w:header="708" w:footer="708" w:gutter="0"/></w:sectPr>' +
    '</w:body></w:document>'

  const contentTypes =
    '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>' +
    '<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">' +
    '<Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/>' +
    '<Default Extension="xml" ContentType="application/xml"/>' +
    '<Override PartName="/word/document.xml" ContentType="application/vnd.openxmlformats-officedocument.wordprocessingml.document.main+xml"/>' +
    '<Override PartName="/word/styles.xml" ContentType="application/vnd.openxmlformats-officedocument.wordprocessingml.styles+xml"/>' +
    '</Types>'

  const rels =
    '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>' +
    '<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">' +
    '<Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" Target="word/document.xml"/>' +
    '</Relationships>'

  const wordRels =
    '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>' +
    '<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">' +
    '<Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/styles" Target="styles.xml"/>' +
    '</Relationships>'

  const stylesXml =
    '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>' +
    '<w:styles xmlns:w="http://schemas.openxmlformats.org/wordprocessingml/2006/main">' +
    '<w:docDefaults><w:rPrDefault><w:rPr><w:rFonts w:ascii="Calibri" w:hAnsi="Calibri" w:cs="Arial"/></w:rPr></w:rPrDefault></w:docDefaults>' +
    '</w:styles>'

  const enc = new TextEncoder()
  return zipStore([
    { name: '[Content_Types].xml', data: enc.encode(contentTypes) },
    { name: '_rels/.rels', data: enc.encode(rels) },
    { name: 'word/document.xml', data: enc.encode(documentXml) },
    { name: 'word/_rels/document.xml.rels', data: enc.encode(wordRels) },
    { name: 'word/styles.xml', data: enc.encode(stylesXml) },
  ])
}

