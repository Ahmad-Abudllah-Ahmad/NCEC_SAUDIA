export function downloadWord(title: string, template: string, type: string) {
  const blob = buildDocxBlob(title, template, type)
  const filename = `${safeFile(title)}.docx`

  // Legacy Edge / IE
  const nav = navigator as Navigator & { msSaveOrOpenBlob?: (b: Blob, n: string) => void }
  if (nav.msSaveOrOpenBlob) {
    nav.msSaveOrOpenBlob(blob, filename)
    return
  }

  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = filename
  a.style.display = 'none'
  document.body.appendChild(a)
  a.click()
  document.body.removeChild(a)
  setTimeout(() => URL.revokeObjectURL(url), 10000)
}

export function downloadPdf(title: string, template: string, type: string) {
  const html = buildHtml(title, template, type, true)
  const win = window.open('', '_blank')
  if (!win) return
  win.document.open()
  win.document.write(html)
  win.document.close()
}
